public class primitive {
    public static void main(String[] args) {
        int a = 999999999;
        char b = 'A';
        long c = 54654546545464l;
        float d = 656.65f;
        boolean e = true;

        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
        System.out.println(e);
    }
}
